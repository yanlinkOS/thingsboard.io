// `mdi:`-prefixed names are fetched from the IoT Hub asset endpoint; any
// other namespace coming from the API will fall back to Material Icons.
const svgIconNamespaces = new Set(['mdi']);

/**
 * Mirrors the platform helper of the same name in
 * /data/git/iot-hub/ui/src/app/shared/models/icon.models.ts. Returns true
 * when `icon` should be rendered as an SVG — either via a namespace
 * (currently just `mdi:`) or via a bare name registered in `svgIcons`.
 */
export function isSvgIcon(icon: string): boolean {
	const colonIdx = icon.indexOf(':');
	if (colonIdx === -1) {
		return icon in svgIcons;
	}
	const namespace = icon.slice(0, colonIdx);
	return svgIconNamespaces.has(namespace);
}

